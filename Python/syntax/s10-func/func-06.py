"""
함수 기본값

"""

def move(x,y):
    print(f"move:x={x}, y={y}")
    
    
"""
인자:함수를 호출할때 전달하느 값
파라미터:함수에 정의되어있는 인자를 받을 변수
호출:인자가 파라미터의 순서대로 전달

"""
move(10,20)
move(x=50,y=100)#(x=50,100) 이런식으로 사용 불가함
move(y=60, x=80)#순서를 바꾸면 헷갈리므로 권장하지 않음


#%%

move("홍길동", 34)

