#기본값
def mo(x,y,z=5):#기본값 일부지정시 뒤에서 부터 지정
    print(f"mo:{x},{y},{z}")
    
    
mo(10, 20)
#파라미터 일부지정시 뒤에서 부터 지정 가능
#ex) mo(10,20,z=30)