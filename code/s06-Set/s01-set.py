# 집합 자료형 : set
# 합집합, 교집합, 여집합, ...
# 세트(set)은 키(key)만 있는 딕셔너리(dict) 형태
# 중복을 허용하지 않는다.
#   - True는 1과 같은 값으로 취급
#   - False는 0과 같은 값으로 취급
# 순서가 없다.(Unordered)
#   - 데이터를 넣은 대로 저장되지 않는다.
# 출력된 형태 : {1,3,5}

#%%

# 빈 리스트
l1 = []
l2 = list()
#%%

# 빈 세트
s = set()
print(type(s), s)


#%%

# 빈 딕셔너리를 빈 세트으로 변환하여 생성
s0 = set({})
print(type(s0), s0)

#%%

# 세트(set)이 아니라 딕셔너리(dict)이다.
s2 = {}
print(type(s2))

#%%


s3 = { 1,3,5,7,9 }
print(type(s3), s3)
