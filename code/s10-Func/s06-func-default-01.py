# 함수 
# 기본값
# 파라미터(parameter): 함수에 정의되어 있는 인자를 받을 변수
# 인자(argument): 함수를 호출할 때 전달하는 값

# 함수정의
# 파라미터: 함수에 정의되어 있는 인자를 받을 변수
def move(x, y):
    print(f"move:x={x}, y={y}")
    z = x + y
    print(f"z={z}")
    
#%%    
# 인자: 함수를 호출할 때 전달하는 값
# 호출 : 인자가가 파라미터의 순서대로 전달
move(10, 20)    

# 파라미터를 지정하여 호출
move(x=100, y=200)
move(y=50, x=60)   # 권장하지 않음: 순서를 바꿈면 혼란

#%%
# move("홍길동", 34)
move("홍길동", "34")


