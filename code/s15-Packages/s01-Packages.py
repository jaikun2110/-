# 패키지(package) : 폴더(folder)
# 여러 모듈을 하나의 폴더에 모아 놓은 집합

# 패키지에 있는 모듈을 사용할 때
# import 폴더.폴더.모듈
# 폴더.폴더.모듈.함수(...)

# from~import
# from 폴더.폴더 import 모듈
# 모듈.함수(...)

# from 폴더.폴더.모듈 import 기능(함수, ...)
# 함수(...)

# 최상위 모듈만 지정
# import 폴더1
# 폴더1.폴더2.모듈.함수(...)

# 패키지에 있는 모든 모듈
# from 폴더.폴더 import *
# 모듈.함수(...)