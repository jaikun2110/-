# 크기비교의 결과 bool
# 크다 : a > b
# 작다 : a < b
# 크거나 같다: a >= b
# 작거나 같다: a <= b

a = 10
b = 20
c = 15

#%%
print(a > b) # False
print(a < b) # True

#%%
print(a >= c) # False
print(a <= c) # True

#%%
print(c >= c) # True
print(c <= c) # True

#%%
# True, False 크기 비교
# True  : 1
# False : 0
print(True > False) # True
print(True < False) # False

