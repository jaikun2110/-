# ESCAPE Character
# 백슬래시(\) 기호를 사용

msg = "철수는 영수에게 \안녕\이라고 말했다."
print(msg)

#%%
# 백슬래시를 출력하려면?
# 연속해서 백스래시를 두 번 기술: \\
eng = "He is always saying \\Could you say is again?\\"
print(eng)

# eng = "He is always saying "Could you say is again?""
eng = "He is always saying \"Could you say is again?\""
print(eng)

# 큰따옴표와 작은따옴표를 혼용해서
# 따옴표를 출력 문자로 처리 가능
eng = 'He is always saying "Could you say is again?"'
print(eng)

eng = "He is always saying 'Could you say is again?'"
print(eng)
